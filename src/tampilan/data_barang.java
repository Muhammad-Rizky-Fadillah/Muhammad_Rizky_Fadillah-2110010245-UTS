    public void dataTable() {
        Object[] Baris = {"No", "Tanggal", "Kode Barang", "Nama Barang", "Jumlah", "Keterangan"};
        tabmode = new DefaultTableModel(null, Baris);
        tblBarang.setModel(tabmode);
        String sql = "select * from tbl_barang order by kode_barang asc";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                String tanggal = hasil.getString("tanggal");
                String kode_barang = hasil.getString("kode_barang");
                String nama_barang = hasil.getString("nama_barang");
                String jumlah = hasil.getString("jumlah");
                String keterangan = hasil.getString("keterangan");
                String[] data = {"", tanggal, kode_barang, nama_barang, jumlah, keterangan};
                tabmode.addRow(data);
                noTable();
                lebarKolom();
            }
        } catch (SQLException e) {
        }
    }

    public void pencarian(String sql) {
        Object[] Baris = {"No", "Tanggal", "Kode Barang", "Nama Barang", "Jumlah", "Keterangan"};
        tabmode = new DefaultTableModel(null, Baris);
        tblBarang.setModel(tabmode);
        int brs = tblBarang.getRowCount();
        for (int i = 0; 1 < brs; i++) {
            tabmode.removeRow(1);
        }
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                String tanggal = hasil.getString("tanggal");
                String kode_barang = hasil.getString("kode_barang");
                String nama_barang = hasil.getString("nama_barang");
                String jumlah = hasil.getString("jumlah");
                String keterangan = hasil.getString("keterangan");
                String[] data = {"", tanggal, kode_barang, nama_barang, jumlah, keterangan};
                tabmode.addRow(data);
                noTable();
            }
        } catch (SQLException e) {

        }
    } 
