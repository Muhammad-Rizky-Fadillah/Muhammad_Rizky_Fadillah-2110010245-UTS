    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try {
            try (BufferedWriter out = new BufferedWriter(new FileWriter("data_barang.txt"))) {
                for (int row = 0; row < tabmode.getRowCount(); row++) {
                    for (int col = 0; col < tabmode.getColumnCount(); col++) {
                        out.write(tabmode.getValueAt(row, col).toString());
                        if (col < tabmode.getColumnCount() - 1) {
                            out.write("\t");  // Tambahkan tab untuk pemisah kolom
                        }
                    }
                    out.write("\n");  // Tambahkan baris baru untuk pemisah baris
                }
                JOptionPane.showMessageDialog(null, "Berhasil Disimpan dalam File");
            }
        } catch (IOException e) {
            System.err.println("Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed


